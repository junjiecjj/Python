               27/20 6/5 1/2 4/5 1/2 0 7/10 4/5 0 1/20 ...
               1/10 1/20 4/5 1/10 4/5 1/10 9/10 3/5 6/5 9/20 ...
               9/20 0 27/20 6/5 2/5 6/5 7/5 3/5 0 9/5]';
